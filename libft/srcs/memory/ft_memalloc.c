#include "../../includes/struct.h"
#include "../../includes/prototypes.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void	*memory;

	if (!(memory = (void*)malloc(sizeof(memory) * size)))
		return (NULL);
	ft_memset(memory, 0, size);
	return (memory);
}
